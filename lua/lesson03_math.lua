-- Lesson 3: Math is Fun!
-- Lua is like a super calculator!

-- Basic math operations
print("Addition: 5 + 3 =", 5 + 3)
print("Subtraction: 10 - 4 =", 10 - 4)
print("Multiplication: 6 * 7 =", 6 * 7)
print("Division: 15 / 3 =", 15 / 3)

-- Let's solve some real problems!
cookies_in_jar = 24
friends = 6
cookies_per_friend = cookies_in_jar / friends
print("Each friend gets", cookies_per_friend, "cookies")

-- More fun math
allowance = 5
weeks = 4
total_money = allowance * weeks
print("In", weeks, "weeks, I'll have $" .. total_money)

-- The .. symbol joins text together!
first_name = "Super"
last_name = "Coder"
full_name = first_name .. " " .. last_name
print("My superhero name is", full_name)

-- Math with decimals
height = 4.5
print("I am", height, "feet tall")

